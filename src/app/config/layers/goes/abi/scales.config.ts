interface AbiRangePaletteConfig {
  readonly min: number;
  readonly max: number;
  readonly unit: string;
  readonly colors: readonly string[];
}

const ABI_PALETTE_SIZE = 256;

function buildDiscreteScaleFromRange(config: AbiRangePaletteConfig): LayerScale {
  const { min, max, unit, colors } = config;
  const lastIndex = colors.length - 1;

  if (colors.length === 0) {
    return {
      type: ScaleType.DISCRETE,
      unit,
      steps: [],
    };
  }

  const steps = colors.map((color, index) => {
    const ratio = lastIndex > 0 ? index / lastIndex : 0;
    const value = min + ratio * (max - min);
    return { value: Number(value.toFixed(8)), color };
  });

  return {
    type: ScaleType.DISCRETE,
    unit,
    steps,
  } as const satisfies LayerScale;
}

